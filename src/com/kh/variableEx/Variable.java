package com.kh.variableEx;
/*
* 변수 : 메모리에 값 저장하는 공간
* 기본자료형
* boolean 1b -참/거짓
* byte 1b 숫자뒤 b
* short 2b 숫자뒤 sh
* int 4b -정수 기본
* long 8b 숫자뒤 l
* float 4b - 숫자뒤 f
* double 8b - 실수 기본
* char 2b 문자하나, 만자 양옆에 ''
*
*
* 참조자료형
* String = 작성한만큼 크기증가
* 양옆에 "" 첫글자 대문자 S
*
* 변수선언: 메모리에 공간생성
*
* */
public class Variable {
    public static void main(String[] args) {


        boolean isTrue; // boolean 저장공간 1바이트 생성
        isTrue = true; //값 대입, 덮어쓰기

        int num1 = 100; //변수선언과 초기값 설정
        
    }
}
